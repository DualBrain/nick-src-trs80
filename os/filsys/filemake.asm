;Filemake: Create a file in the tree.
;
	ORG	5300H
*GET	FILSYS
;
START	LD	SP,5300H
	CALL	FS_INIT
	JP	NZ,FS_DOS_ERROR
	CALL	FS_TERMINATE
	LD	DE,FCB
	LD	HL,BUF
	LD	B,0
	CALL	FS_OPEN_NEW
	JP	NZ,FS_DOS_ERROR
	CALL	FS_CLOSE
	JP	NZ,FS_DOS_ERROR
	CALL	FS_EXIT
	JP	NZ,FS_DOS_ERROR
	JP	FS_DOS
;
FCB	DEFS	32
BUF	DEFS	256
;
	END	START
