	DI
	CALL	SWAP	;SWAP FOR ORIGINAL CODE.
	JP	4380H
SWAP	LD	HL,4000H
	LD	IX,8000H
	LD	BC,11FFH
SWLOOP	LD	D,(HL)
	LD	A,(IX+0)
	LD	(HL),A
	LD	(IX+0),D
	INC	HL
	INC	IX
	DEC	BC
	LD	A,B
	OR	C
	JR	NZ,SWLOOP
	RET
	END	START
